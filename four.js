const num4 = prompt("Введіть, будь ласка число від 1 до 100.");

numbersRange(num4);

function numbersRange(num4) {
  const numParse4 = Number.isFinite(parseFloat(num4));

  if (numParse4 === false) {
    alert("Ви ввели не число.");
  } else if (numParse4 === true) {
    const n = parseInt(num4);

    if (n === 0) {
        alert("Вибачте, але ви не ввели число, яке від 1 до 100.");
      } else if (n < 26) {
      alert("Дякую, за вашу відповідь, ваше число знаходиться в першій четверті.");
    } else if (n < 51) {
        alert("Дякую, за вашу відповідь, ваше число знаходиться в другій четверті.");
    } else if (n < 76) {
      alert("Дякую, за вашу відповідь, ваше число знаходиться в третій четверті.");
    } else if (n <= 100) {
      alert("Дякую, за вашу відповідь, ваше число знаходиться в четвертій четверті.");
    } else {
      alert("Вибачте, але ви не ввели число, яке від 1 до 100.");
    }
  }
}

