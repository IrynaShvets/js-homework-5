const num7 = prompt("Введіть, будь ласка число.");

result(num7);

function result(num7) {
  const n7 = parseInt(num7);

  for (let i = 1; i <= 100; i++) {
    alert(
      `Додавання: ${n7 + i}; віднімання: ${n7 - i}; множення: ${
        n7 * i
      }; ділення: ${n7 / i};`
    );
  }
}

